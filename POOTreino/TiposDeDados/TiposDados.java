package SegundoAno.POOTreino.TiposDeDados;

public class TiposDados {
    public static void main(String[] args) {
        // tipos mais usados em Java
        int idade = 20;
        double altura = 1.75;
        boolean jovem = true;
        char letra = 'a';
        String nome =  "Fulano";

        System.out.println("A idade é: " + idade);
        System.out.println("A altura é: " + altura);
        System.out.println("A pessoa é jovem?: " + jovem);
        System.out.println("A letra digitada é: " + letra);
        System.out.println("O nome é: " + nome);
    }
}